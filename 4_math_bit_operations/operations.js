// Math and Bit Operation 
// in JavaScript

// Multiplication
let result_mul = 3 * 3
console.log(result_mul) // 9

// Power
let result_pow = 3 ** 3
console.log(result_pow) // 27

// Addition
let result_add = 3 + 3
console.log(result_add) // 6

// Subtraction
let result_sub = 3 - 3
console.log(result_sub) // 0

// Division
let result_div = 3 / 3
console.log(result_div) // 1

// Bitwise Right Shift
let result_bit_r = 3 >> 3
console.log(result_bit_r) // 0

// Bitwise Left Shift
let result_bit_l = 3 << 3
console.log(result_bit_l) // 24

// Bitwise OR
let result_bit_or = 3 | 9
console.log(result_bit_or) // 11

// Bitwise AND
let result_bit_and = 3 & 9
console.log(result_bit_and) // 1