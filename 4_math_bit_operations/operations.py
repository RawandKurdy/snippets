# Math and Bit Operation 
# in Python

# Multiplication
result_mul = 3 * 3
print(result_mul) # 9

# Power
result_pow = 3 ** 3
print(result_pow) # 27

# Addition
result_add = 3 + 3
print(result_add) # 6

# Subtraction
result_sub = 3 - 3
print(result_sub) # 0

# Division (with Decimals)
result_div_d = 3 / 3
print(result_div_d) # 1.0

# Division (No Decimals)
result_div = 3 // 3
print(result_div) # 1


# Bitwise Right
result_bit_r = 3 >> 3
print(result_bit_r) # 0

# Bitwise Left
result_bit_l = 3 << 3
print(result_bit_l) # 24

# Bitwise OR
result_bit_or = 3 | 9
print(result_bit_or) # 11

# Bitwise AND
result_bit_and = 3 & 9
print(result_bit_and) # 1